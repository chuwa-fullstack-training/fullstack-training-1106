// Addition
let additionResult = 5 + 3;
console.log(additionResult); // Output: 8

// Subtraction
let subtractionResult = 10 - 4;
console.log(subtractionResult); // Output: 6

// Multiplication
let multiplicationResult = 2 * 6;
console.log(multiplicationResult); // Output: 12

// Division
let divisionResult = 15 / 3;
console.log(divisionResult); // Output: 5

// Modulo (Remainder)
let moduloResult = 17 % 4;
console.log(moduloResult); // Output: 1

// Exponentiation
let exponentiationResult = 2 ** 3;
console.log(exponentiationResult); // Output: 8

// Increment
let incrementValue = 5;
incrementValue++;
console.log(incrementValue); // Output: 6

// Decrement
let decrementValue = 8;
decrementValue--;
console.log(decrementValue); // Output: 7

// Assignment Operators
let x = 10;

x += 5;
console.log('Addition Assignment:', x); // Output: 15

x -= 3;
console.log('Subtraction Assignment:', x); // Output: 12

x *= 2;
console.log('Multiplication Assignment:', x); // Output: 24

x /= 3;
console.log('Division Assignment:', x); // Output: 8

x %= 5;
console.log('Modulo Assignment:', x); // Output: 3

x **= 2;
console.log('Exponentiation Assignment:', x); // Output: 9

// Comparison Operators
let a = 5;
let b = 10;

let equal = a == b;
console.log('Equal:', equal); // Output: false

let notEqual = a != b;
console.log('Not Equal:', notEqual); // Output: true

let greaterThan = a > b;
console.log('Greater Than:', greaterThan); // Output: false

let lessThan = a < b;
console.log('Less Than:', lessThan); // Output: true

let greaterThanOrEqual = a >= b;
console.log('Greater Than or Equal:', greaterThanOrEqual); // Output: false

let lessThanOrEqual = a <= b;
console.log('Less Than or Equal:', lessThanOrEqual); // Output: true

// Logical Operators
let p = true;
let q = false;

let logicalAnd = p && q;
console.log('Logical AND:', logicalAnd); // Output: false

let logicalOr = p || q;
console.log('Logical OR:', logicalOr); // Output: true

let logicalNotP = !p;
console.log('Logical NOT (p):', logicalNotP); // Output: false

let logicalNotQ = !q;
console.log('Logical NOT (q):', logicalNotQ); // Output: true

// Ternary Operator
let age = 20;
let isAdult = age >= 18 ? 'Adult' : 'Not Adult';
console.log('Is Adult:', isAdult); // Output: "Adult"
